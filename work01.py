import math

print("=== โปรแกรมคำนวณพื้นที่หลายรูปทรง ===")
print("1. สี่เหลี่ยมจัตุรัส")
print("2. สี่เหลี่ยมผืนผ้า")
print("3. สามเหลี่ยม")
print("4. วงกลม")

รูปทรง = int(input("\nเลือกรูปทรง (1-4): "))

if รูปทรง == 1:
    กว้าง = float(input("กรอกความกว้าง (หน่วย): "))
    
    พื้นที่ = กว้าง ** 2

    print("\n===== ผลลัพธ์ =====")
    print("รูปทรง : สี่เหลี่ยมจัตุรัส")
    print("ความกว้าง :", กว้าง, "หน่วย")
    print("พื้นที่ :", พื้นที่, "ตารางหน่วย")

elif รูปทรง == 2:
    กว้าง = float(input("กรอกความกว้าง (หน่วย): "))
    ยาว = float(input("กรอกความยาว (หน่วย): "))

    พื้นที่ = กว้าง * ยาว

    print("\n===== ผลลัพธ์ =====")
    print("รูปทรง : สี่เหลี่ยมผืนผ้า")
    print("ความกว้าง :", กว้าง, "หน่วย")
    print("ความยาว :", ยาว, "หน่วย")
    print("พื้นที่ :", พื้นที่, "ตารางหน่วย")

elif รูปทรง == 3:
    กว้าง = float(input("กรอกความกว้างฐาน (หน่วย): "))
    ยาว = float(input("กรอกความสูง (หน่วย): "))

    พื้นที่ = 0.5 * กว้าง * ยาว

    print("\n===== ผลลัพธ์ =====")
    print("รูปทรง : สามเหลี่ยม")
    print("ความกว้างฐาน :", กว้าง, "หน่วย")
    print("ความสูง :", ยาว, "หน่วย")
    print("พื้นที่ :", พื้นที่, "ตารางหน่วย")

elif รูปทรง == 4:
    กว้าง = float(input("กรอกรัศมี (หน่วย): "))

    พื้นที่ = math.pi * (กว้าง ** 2)

    print("\n===== ผลลัพธ์ =====")
    print("รูปทรง : วงกลม")
    print("รัศมี :", กว้าง, "หน่วย")
    print("พื้นที่ :", round(พื้นที่, 2), "ตารางหน่วย")

else:
    print("\nกรุณาเลือกรูปทรงให้ถูกต้อง (1-4)")
